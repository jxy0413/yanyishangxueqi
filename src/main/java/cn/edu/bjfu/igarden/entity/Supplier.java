public class Supplier {
    private int supplierid;
    private String supplier_name;
    private String supplier_phone;
    private String supplier_address;
    private String supplier_flower;
    private String supplier_qq;
    private String supplier_email;
    private String supplier_image;
    public int getSupplierid() {
        return supplierid;
    }

    public void setSupplierid(int supplierid) {
        this.supplierid = supplierid;
    }

    public String getSupplier_name() {
        return supplier_name;
    }

    public void setSupplier_name(String supplier_name) {
        this.supplier_name = supplier_name;
    }

    public String getSupplier_phone() {
        return supplier_phone;
    }

    public void setSupplier_phone(String supplier_phone) {
        this.supplier_phone = supplier_phone;
    }

    public String getSupplier_address() {
        return supplier_address;
    }

    public void setSupplier_address(String supplier_address) {
        this.supplier_address = supplier_address;
    }

    public String getSupplier_flower() {
        return supplier_flower;
    }

    public void setSupplier_flower(String supplier_flower) {
        this.supplier_flower = supplier_flower;
    }

    public String getSupplier_qq() {
        return supplier_qq;
    }

    public void setSupplier_qq(String supplier_qq) {
        this.supplier_qq = supplier_qq;
    }

    public String getSupplier_email() {
        return supplier_email;
    }

    public void setSupplier_email(String supplier_email) {
        this.supplier_email = supplier_email;
    }

    public String getSupplier_image() {
        return supplier_image;
    }

    public void setSupplier_image(String supplier_image) {
        this.supplier_image = supplier_image;
    }
}
