public class ExpertSolr {

    private int expertsid;
    private String expertsclassification;
    private String expertsachievement;
    private String expertstype;

    public int getExpertsid() {
        return expertsid;
    }

    public void setExpertsid(int expertsid) {
        this.expertsid = expertsid;
    }

    public String getExpertsclassification() {
        return expertsclassification;
    }

    public void setExpertsclassification(String expertsclassification) {
        this.expertsclassification = expertsclassification;
    }

    public String getExpertsachievement() {
        return expertsachievement;
    }

    public void setExpertsachievement(String expertsachievement) {
        this.expertsachievement = expertsachievement;
    }

    public String getExpertstype() {
        return expertstype;
    }

    public void setExpertstype(String expertstype) {
        this.expertstype = expertstype;
    }
}
